def send_to_physician(receiver, report, reasons):
    """Handles secure SMTP transmission to the physician."""
    msg = EmailMessage()
    msg['Subject'] = f"ð¨ {report['Risk']} Clinical Alert - {report['Name']}"
    msg['From'] = st.secrets.get("EMAIL_USER", "")
    msg['To'] = receiver
    
    body = f"""OFFICIAL CLINICAL HANDOVER
-------------------------------
PATIENT: {report['Name']} ({report['Age']}yr {report['Gender']})
PRIMARY PREDICTION: {report['Disease']} ({report['Prob']}%)
TRIAGE CATEGORY: {report['Risk']}

VITAL SIGNS ANALYSIS:
{chr(10).join(['- ' + r for r in reasons])}

This patient data was processed via the Clinical AI CDSS.
-------------------------------
"""
    msg.set_content(body)
    try:
        with smtplib.SMTP_SSL('smtp.gmail.com', 465) as smtp:
            smtp.login(st.secrets["EMAIL_USER"], st.secrets["EMAIL_PASS"])
            smtp.send_message(msg)
        return True
    except:
        return False

def get_triage_status(disease, prob, spo2, bps):
    """Advanced Triage Logic: Combines AI probability with physiological safety limits."""
    if spo2 < 88 or bps > 185 or (disease != "Normal" and prob > 94):
        return "ð´ CRITICAL", "Immediate Physician Intervention Required", "#ff4b4b"
    elif prob > 70 or spo2 < 93:
        return "ð¡ URGENT", "Priority Nursing Assessment Required", "#ffa500"
    else:
        return "ð¢ STABLE", "Routine Monitoring & Follow-up", "#28a745"

